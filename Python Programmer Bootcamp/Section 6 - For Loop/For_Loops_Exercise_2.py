''' Step 1: Create a list of numbers containing 
integers [10, 13, 25, 32, 45, 66, 68]'''

List = [10, 13, 25, 32, 45, 66, 68]


# Step 2: Access and print the first and last elements

print("First element:", List[0])
print("Last element:", List[-1])

# Step 3: Slice the list to print the first 3 elements
print("First 3 elements:", (List[0:3]))

# Step 4: Iterate through the list and print each element
for i in List:
    print(i)
    
# Step 5: Calculate the sum of all elements in the list
print("Sum of all elements:", sum(List))




