# Step 1: Create the string variable
my_string = "Learning"

# Step 2: Print the length of the string
print(len(my_string))

# Step 3: Print the first and last characters
print(my_string[0]) # First character

print(my_string[-1]) # Last character

# Step 4: Slice and print the first 4 characters
print(my_string[0:4])

# Step 5: Slice and print the last 3 characters using negative indexing
print(my_string[5:])